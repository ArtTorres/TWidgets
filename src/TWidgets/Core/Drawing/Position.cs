namespace TWidgets.Core.Drawing
{
    /// <summary>
    /// Specifies constants that define the display position of widgets.
    /// </summary>
    public enum Position
    {
        /// <summary>
        /// Displays the widget with the top of the screen as reference.
        /// </summary>
        Absolute,

        /// <summary>
        /// Displays the widget line after line.
        /// </summary>
        Relative,

        /// <summary>
        /// Displays the widget in the same place.
        /// </summary>
        Fixed
    }
}
